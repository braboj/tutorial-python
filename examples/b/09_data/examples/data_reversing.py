# Example: Filter Data using the filter() function

# Sample data
sample_1 = [1, 2, 5, 4, 3]
sample_2 = {1: 'a', 2: 'b', 5: 'd', 4: 'c', 3: 'e'}

# Reverse a list
iterator = reversed(sample_1)
print(list(iterator))

# Reverse a dictionary
iterator = reversed(sample_2.items())
print(list(iterator))

