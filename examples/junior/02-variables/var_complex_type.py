# Complex variables
# -----------------------------------------------------------------------------
# This code demonstrates how to create and manipulate complex numbers in Python.
# Complex numbers are represented as `a + bj`, where `a` is the real part and
# `b` is the imaginary part.

z1 = 1 + 2j
z2 = 2 + 4j
print(z1, z2)
# Output:
# (1+2j) (2+4j)
