# Example: Assignment Operators

# Initialize variables
a = 10
b = 20

# Assign
test = a
print(test)

# Bitwise AND and Assign
test &= b
print(test)

# Bitwise OR and Assign
test |= b
print(test)

# Bitwise XOR and Assign
test ^= b
print(test)

# Left Shift and Assign
test <<= b
print(test)

# Right Shift and Assign
test >>= b
print(test)

# Add and Assign
test += b
print(test)

# Subtract and Assign
test -= b
print(test)

# Multiply and Assign
test *= b
print(test)

# Divide and Assign
test /= b
print(test)

# Floor Divide and Assign
test //= b
print(test)

# Modulus and Assign
test %= b
print(test)

# Exponentiation and Assign
test **= b
print(test)
