# Example: Arithmetic Operators

# Initialize variables
a, b = 10, 20

# Sum
print(a+b)

# Subtraction
print(a-b)

# Multiplication
print(a*b)

# Division (Python 3: float, Python 2: int)
print(a/b)

# Floor Division (integer division)
print(a//b)

# Modulus (remainder)
print(a % b)

# Exponentiation (a to the power of b)
print(a**b)

