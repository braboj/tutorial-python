# String concatenation example
# ------------------------------------------------------------------------------
# This code concatenates (joins) strings using the `+` operator. This
# technique is common when constructing messages or combining text from
# different sources.


string1 = 'Hello'
string2 = "World"
string3 = "!"

result = string1 + " " + string2 + string3
print(result)
# Output:
# Hello World!
