# String case manipulation examples
# -----------------------------------------------------------------------------
# This code applies various string case manipulation methods in Python.

# Store the string to be manipulated
text = "AAAA bbbb"

print(text.upper())
# Output:
# AAAA BBBB

print(text.lower())
# Output:
# aaaa bbbb

print(text.title())
# Output:
# Aaaa Bbbb

print(text.capitalize())
# Output:
# Aaaa bbbb

print(text.swapcase())
# Output:
# aaaa BBBB
