# Reason of having variables in Python
# -----------------------------------------------------------------------------
# This code is used to demonstrate why we need variables in Python. For example,
# without them, we would have to repeat the same code multiple times and
# change it manually every time we want to change the value. Imagine if we
# wanted to print "Hello world" 1000 times and then change it to "Hello
# Python" - we would have to change it in every place where we used it.

print("Hello world")
print("Hello world")
print("Hello world")
print("Hello world")
print("Hello world")
# Output: Hello world
